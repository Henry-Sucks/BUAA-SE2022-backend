<template>
  <div>
    <el-row>
      <el-col :span="3"></el-col>
      <el-col :span="10">
        <h1 class="text">
          <div class="text-anime">
            <span class="typed-text" v-html="typeValue"></span>
            <span class="cursor" :class="{'typing' : typeStatus}">&nbsp;</span>
          </div>
          <span style="color:#4A848C">沃扣</span>
          <span>：新一代软件工程协作与管理平台</span>
        </h1>
      </el-col>
    </el-row>
    <el-row>
      <el-col :span="3"></el-col>
      <el-col :span="6">
      <register-form></register-form>
      </el-col>
    </el-row>
  </div>
</template>

<script>
const pic1 = require('../assets/test1.jpg')
const pic2 = require('../assets/test2.jpg')
const pic3 = require('../assets/test3.jpg')

import {UserFilled, Lock} from '@element-plus/icons-vue'
import axios from 'axios'
import RegisterForm from '../components/RegisterForm.vue'
export default {
  name: 'RegisterView',

  components: {
    Lock, UserFilled, RegisterForm
  },

  

  data() {
    return {
      info: 
        {
          userName : '',
          email : '',
          password : '',
        },
      passwordAgain: '',

      pics : [
        pic1, pic2, pic3
      ],

      // 打字动画
      typeValue: '',
      typeStatus: false,
      typeContent: 'WorkCo',
      typingSpeed: 300,
      erasingSpeed: 100,
      newTextDelay: 2000,
      charIndex: 0,
      textColors: ['#2F77B5', '#D94437', '#EAB52A', '#2F77B5', '#489F4D', '#D94437', null, null]

    }
  },

  methods: {
    // 发送数据
    handleRegister(){
        axios({
          method: 'GET',
            url: 'http://localhost:9090/user/findEmail',
            params: {
              userEmail: this.info.email,
            }
          }).then(response => {
            let res = response.data
            if(JSON.stringify(res) == "{}")
            {
              console.log("邮箱", res)
              alert('已存在该用户!')
            }   
            else
            {
              axios({
                method: 'GET',
                url: 'http://localhost:9090/user/findName',
                params: {
                  userName: this.info.userName,
                }
              }).then(response => {
                let res = response.data
                if(JSON.stringify(res) == "{}")
                {
                  console.log("用户名", res)
                  alert('已存在该用户!')
                }
                else
                {
                  if(this.info.password !== this.passwordAgain)
                  {
                    alert("两次密码输入不一致！")
                    return
                  }
                  if(this.info.userName !== "" && this.info.email !== "" && this.info.passWord !== "")
                  axios({
                    method: 'POST',
                    url: 'http://localhost:9090/user',
                    params: {
                      userName: this.info.userName,
                      Email: this.info.email,
                      passWord: this.info.password,
                    }
                  }).then(response => {
                    console.log(response)
                    this.$store.commit('loginOptions/setLoggedIn')
                  })
                  else
                    alert("请填入完整信息!")
                }          
            })
            }          
        })



        
        },
    

    // 文字动画
    typeText() {
      if(this.charIndex < this.typeContent.length){
        if(!this.typeStatus)
          this.typeStatus = true;
        this.typeValue +=
            '<span style="color:' + this.textColors[this.charIndex] + '">' + this.typeContent.charAt(this.charIndex) + '</span>'
        this.charIndex += 1;
        setTimeout(this.typeText, this.typingSpeed)
      }
      else{
        this.typeStatus = false;
        setTimeout(this.eraseText, this.newTextDelay)
      }
    },

    eraseText() {
      if(this.charIndex > 0){
        if(!this.typeStatus)
          this.typeStatus = true;

        this.typeValue = this.typeContent.substring(0, this.charIndex - 1);
        this.charIndex -= 1;
        setTimeout(this.eraseText, this.erasingSpeed);
      }
      else{
        this.typeStatus = false;

        setTimeout(this.typeText, this.newTextDelay)
      }
    }
  },

  created() {
    setTimeout(this.typeText, this.newTextDelay)
  },
}

</script>

